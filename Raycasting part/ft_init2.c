#include "raycasting.h"

void	ft_init_rays(t_adata *a)
{
	a->ray.fov = 60  * (M_PI / 180);
	a->ray.wall_strip_w = 1;
	a->ray.num_rays = a->win.win_w /a->ray.wall_strip_w;
}
